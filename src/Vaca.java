public class Vaca {
    private int idVaca;
    private int peso;
    private int pLeche;

    public Vaca() {
    }

    public Vaca(int idVaca, int peso, int pLeche) {
        this.idVaca = idVaca;
        this.peso = peso;
        this.pLeche = pLeche;
    }

    public int getIdVaca() {
        return idVaca;
    }

    public void setIdVaca(int idVaca) {
        this.idVaca = idVaca;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public int getpLeche() {
        return pLeche;
    }

    public void setpLeche(int pLeche) {
        this.pLeche = pLeche;
    }
    
    
}
